    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String roomid = jTextField7.getText();
        {
        jComboBox1.removeAllItems();
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField9.setText("");
        
        try{
            
            ResultSet rs = selection.getData("SELECT * FROM transaction where roomid = '"+roomid+"'");
            while(rs.next()){
                jTextField1.setText(rs.getString("userid"));
                jComboBox1.addItem(rs.getString("roomtype"));
                jComboBox1.setSelectedItem(rs.getString("roomtype"));
                Date checkIn = rs.getDate("checkindate");
                SimpleDateFormat myFormat = new SimpleDateFormat("yyyy-MM-dd");
                String dateBeforestr = myFormat.format(checkIn);
                jTextField5.setText(dateBeforestr);
                java.util.Date dateBefore = myFormat.parse(dateBeforestr);
                String dateAfterstr = jTextField6.getText();
                java.util.Date dateAfter = myFormat.parse(dateAfterstr);
                long difference = dateAfter.getTime() - dateBefore.getTime();
                int noOfDay = (int)(difference/(1000*60*60*24));
                jTextField8.setText(String.valueOf(noOfDay));
                
            }
            
            rs = selection.getData("SELECT * FROM users where userid = '"+jTextField1.getText()+"'");
            while(rs.next()){
                jTextField2.setText(rs.getString(2));
                jTextField3.setText(rs.getString(5));
                jTextField4.setText(rs.getString(6));
            }
                        
                        
            rs = selection.getData("SELECT * FROM room where roomid = '"+jTextField7.getText()+"'");
            while(rs.next()){
                jTextField9.setText(Double.toString(rs.getDouble(5)));
                double price = Double.parseDouble(jTextField9.getText());
                int noOfDay = Integer.parseInt(jTextField8.getText());
                jTextField10.setText(String.valueOf((double)noOfDay * price));
            }
        }catch(Exception e){
            
            JOptionPane.showMessageDialog(null, e);
        }
        }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

