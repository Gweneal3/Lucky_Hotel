    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try {
            // TODO add your handling code here:
            DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
            int selectedRowIndex = jTable1.getSelectedRow();
            
            jTextField1.setText(model.getValueAt(selectedRowIndex,0).toString());
            jTextField2.setText(model.getValueAt(selectedRowIndex,1).toString());
            jTextField3.setText(model.getValueAt(selectedRowIndex,2).toString());
            jTextField4.setText(model.getValueAt(selectedRowIndex,3).toString());
            jTextField7.setText(model.getValueAt(selectedRowIndex,6).toString());
            jComboBox1.setSelectedItem(model.getValueAt(selectedRowIndex,7).toString());
            jTextField5.setText(model.getValueAt(selectedRowIndex,9).toString());
            jTextField9.setText(model.getValueAt(selectedRowIndex,8).toString());
            double price = Double.parseDouble(jTextField9.getText());  
            SimpleDateFormat myFormat=new SimpleDateFormat("dd/MM/yyyy");
            SimpleDateFormat sqlFormat = new SimpleDateFormat("yyyy-MM-dd");       
            String dateBeforestr = jTextField5.getText();
            java.util.Date dateBefore = sqlFormat.parse(dateBeforestr);
            String dateAfterstr = jTextField6.getText();
            java.util.Date dateAfter = sqlFormat.parse(dateAfterstr);
            long difference = dateAfter.getTime() - dateBefore.getTime();
            int noOfDay = (int)(difference/(1000*60*60*24));
            jTextField8.setText(Integer.toString(noOfDay));
            jTextField10.setText(String.valueOf((double)noOfDay * price));
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jTable1MouseClicked

