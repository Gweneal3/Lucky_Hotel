    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int check=0;
        Email=jTextField1.getText();
        if (Email.equals("")){
            check=1;
            JOptionPane.showMessageDialog(null, "your email shouldn't be left blank");
           
        }else{
            ResultSet resultset=selection.getData("select *from users where Email='"+Email+"'");
            try{
                if(resultset.next()){
                    check=1;
                    jTextField1.setEditable(false);
                    jTextField2.setEditable(false);
                    jTextField2.setText(resultset.getString(8));
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }if(check==0){
            JOptionPane.showMessageDialog(null, "No such email registered! Please recheck your email or go for our registration page");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

