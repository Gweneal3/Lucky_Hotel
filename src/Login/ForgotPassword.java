    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int check=0;
        String SecurityQuestion=jTextField2.getText();
        String Answer=jTextField3.getText();
        String Password=jPasswordField1.getText();
        String Passwordreconfirmation=jPasswordField2.getText();
        if (Answer.equals("")||Password.equals("")||Passwordreconfirmation.equals("")){
            JOptionPane.showMessageDialog(null,"All fields are required");
        }else if (!Password.equals(Passwordreconfirmation)){
            JOptionPane.showMessageDialog(null, "Password inserted should be the same");
        }else{
            ResultSet resultset=selection.getData("select *from users where Email='"+Email+"' and SecurityQuestion='"+SecurityQuestion+"' and Answer='"+Answer+"'");
            try{
                if(resultset.next()){
                    check=1;
                    Update.setData("update users set Password='"+Password+"'where Email='"+Email+"'", "Password Reset Run Successfully");
                    setVisible(false);
                    new ForgotPassword().setVisible(true);
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
            if(check==0)
                JOptionPane.showMessageDialog(null, "Your answer did not match. Please try again");
                }
    }//GEN-LAST:event_jButton2ActionPerformed

